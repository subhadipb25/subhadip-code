
public class ArithmeticDemo2 {

	public static void main(String[] args)
	{
	    int m = 4;
	    int n = 5;
	     
	   n++; // n += 1;  // n = n+m 
	   
	   ++n; // pre increment
	   n++; // post increment
	     
	   
	  m = ++n;
	     
         System.out.println(m);
         System.out.println(n);
	}

}
