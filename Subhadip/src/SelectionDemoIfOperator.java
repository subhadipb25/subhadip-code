
public class SelectionDemoIfOperator {

	
	// if operator
	public static void main(String[] args) 
	{
		
		int n =10;
		
		if(true)
		System.out.println("hello");
		
		if(false)
		System.out.println("bye");
		 
		if(n%2==0)
			System.out.println("Even");
		
		if(n%2!=0) // else   // not equal sign!
		
			System.out.println("Odd");
		 
	}

}