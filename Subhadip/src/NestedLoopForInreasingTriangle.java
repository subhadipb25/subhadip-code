public class NestedLoopForInreasingTriangle 
{
	
		public static void main(String[] args) 
	{
		int n = 20;
		{
			for(int i=1;i<=n;i++)  // outer loop is set to 1 to n
			{
			for(int j=1;j<=i;j++)  // Nested loop is set to 1 t i
				
			{
			System.out.print("* ");
	
			}
			{
				System.out.println();
			}

			}
		}
	}
}
	